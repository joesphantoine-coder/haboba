export function getStoryById(id: string): Story | undefined {
  return stories.find(story => story.id === id);
}

export function getStoriesByCategory(category: string): Story[] {
  if (category === 'All') return stories;
  return stories.filter(story => story.category === category);
}

export function searchStories(query: string): Story[] {
  const lowerQuery = query.toLowerCase();
  return stories.filter(story => 
    story.title.toLowerCase().includes(lowerQuery) ||
    story.summary.toLowerCase().includes(lowerQuery) ||
    story.region.toLowerCase().includes(lowerQuery)
  );
}